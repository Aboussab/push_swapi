#include "libft.h"

int	ft_isdigit(char *c)
{
	int	i;

	i = 0;
	while (c[i] != '\0')
	{
		if (!(c[i] >= 48 && c[i] <= 57))
			return (0);
		i++;
	}
	return (1);
}
